function applyLanguage(lang) {
  const dict = translations[lang] || translations.zh;
  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.innerHTML = dict[key];
  });
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });
  localStorage.setItem("studioLanguage", lang);
}

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

document.querySelectorAll(".wechat-link").forEach((link) => {
  link.addEventListener("click", async (event) => {
    const id = link.dataset.wechatId;
    if (!id) return;
    try {
      await navigator.clipboard.writeText(id);
      alert(translations[localStorage.getItem("studioLanguage") || "zh"].wechatCopied);
    } catch {
      alert(id);
    }
    event.preventDefault();
  });
});

applyLanguage(localStorage.getItem("studioLanguage") || "zh");
